#include "main.h"
#include <stdio.h>
#include <stdlib.h>


/**
  * main - prints out its name followed by anew line
  *
  * @argc: length of string
  * @argv: pointer to string
  *
  * Return: always 0 (success)
  */

int main(int argc, char *argv[])
{
	int i;

	for (i = 0; argc < i; i++)
	{
		printf("%s\n", argv[i]);
		return (0);
	}
}
